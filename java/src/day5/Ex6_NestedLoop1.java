package day5;

public class Ex6_NestedLoop1 {

	public static void main(String[] args) {
		/* *****
		 * *****
		 * *****
		 * 
		 * 반복문으로 작성
		 * */
		
		for(int i =1; i <=5; i++) {
			//* 5개 출력하는 코드
			// * 을 5개출력하고 엔터치는 반목문 코드로 수정
			for(int j = 1 ; j <= 5; j++) {
				System.out.print("*");
			}
			System.out.println("");
		
			
		}

	}

}
